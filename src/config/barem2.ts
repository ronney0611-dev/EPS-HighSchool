// always returns a score out of 20
export const getScore = (
    activity: Activity,
    gender: Gender,
    performance: number,
    isThirdYear: boolean
): number => {
    const table = isThirdYear ? bareme14[activity][gender] : bareme[activity][gender];
    if (activity === 'sprint') {
        if (performance <= table[0].performance) return table[0].score;
        for (const entry of table) {
            if (performance <= entry.performance) return entry.score;
        }
        return 0;
    } else {
        if (performance >= table[0].performance) return table[0].score;
        for (const entry of table) {
            if (performance >= entry.performance) return entry.score;
        }
        return 0;
    }
};